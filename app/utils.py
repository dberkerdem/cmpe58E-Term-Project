class ArithmeticOperations:
    @staticmethod
    def add(x, y):
        return x + y

    @staticmethod
    def subtract(x, y):
        return x - y

    @staticmethod
    def multiply(x, y):
        return x * y / 2


class StringManipulations:
    @staticmethod
    def capitalize_string(s):
        return s.capitalize()

    @staticmethod
    def reverse_string(s):
        return s[::-1]
